fn main() {
    // addition
    let sum = 5550.00 + 7310.00;
    println!("The sum of 5550 and 7310 = {} ",sum);

    // subtraction
    let difference = 95.50 - 4.30;
    println!("The difference of 95.5 and 4.3 = {} ",difference);

    // multiplication
    let product:f32 = 4.00 * 30.00;
    println!("The multiplication of 4 and 30 = {} ",product);

    // division
    let quotient = 56.70 / 32.20;
    println!("The division of 56.7 and 32,2 = {} ",quotient);

    // remainder
    let remainder = 43.00 - (5.00 * 8.00);
    println!("The remainder of 43 and 5 = {} ",remainder);
}
